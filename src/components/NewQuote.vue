<template>
  <div>
    <div class="row">
      <form action>
        <div class="col-sm-8 col-sm-offset-2 col-xs-12 col-md-6 col-md-offset-3">
          <label for="newQuote">Quote</label>
          <textarea v-model="quote" name="newQuote" class="form-control" id cols="30" rows="3"></textarea>
        </div>
        <div class="col-sm-8 col-sm-offset-2 col-xs-12 col-md-6 col-md-offset-3">
          <button @click.prevent="createNew()" class="btn btn-primary">Add Quote</button>
        </div>
      </form>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      quote: ""
    };
  },
  methods: {
      createNew() {
          if (this.quote.length > 0)
          {this.$emit('quoteAdded', this.quote)
          this.quote = ''
          }
          else alert('You cannot add a blank quote')
      }
  }
};
</script>

